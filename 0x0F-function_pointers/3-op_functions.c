#include	"3-calc.h"

/**
 * op_add - function returns sum of a and b
 * @a: first integer
 * @b: second integer
 * Return: the sum of a and b
 */

int	op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function returns the diffrence of a and b
 * @a: first integer
 * @b: second integer
 * Return: the diffrence of a and b
 */

int	op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function returns the product of a and b
 * @a: first integer
 * @b: secon integer
 * Return: the product of and b
 */

int	op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function returns the remainder of the division of a by b
 * @a: first integer
 * @b: second integer
 * Return: the remainder of the division of a by b
 */

int	op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function returns the remainder of the division of a by b
 * @a: first integer
 * @b: second integer
 * Return: the remainder of the division of a by b
 */

int	op_mod(int a, int b)
{
	return (a % b);
}
